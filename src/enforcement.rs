#[cfg(target_os = "linux")]
fn maybe_add_iptables_rule(
    program: &str,
    check_args: &[String],
    add_args: &[String],
    out: &mut EnforcementResult,
) -> Result<()> {
    let exists = Command::new(program)
        .args(check_args)
        .status()
        .with_context(|| format!("failed to check rule: {} {:?}", program, check_args))?
        .success();
    if !exists {
        run(program, add_args)?;
        let mut rollback = add_args.to_vec();
        if let Some(op) = rollback.get_mut(2) {
            *op = "-D".to_string();
        }
        out.rollback_commands
            .push(format!("{} {}", program, rollback.join(" ")));
    }
    Ok(())
}

#[cfg(target_os = "linux")]
fn run(cmd: &str, args: &[String]) -> Result<()> {
    let status = Command::new(cmd)
        .args(args)
        .status()
        .with_context(|| format!("failed to run command: {} {:?}", cmd, args))?;
    if !status.success() {
        anyhow::bail!("command failed: {} {:?}", cmd, args);
    }
    Ok(())
}

fn detect_primary_ipv4() -> Result<String> {
    #[cfg(target_os = "linux")]
    {
        let out = Command::new("sh")
            .arg("-c")
            .arg("ip route get 1.1.1.1 | awk '{for(i=1;i<=NF;i++) if($i==\"src\") {print $(i+1); exit}}'")
            .output()
            .context("failed to run ip route query")?;
        let ip = String::from_utf8_lossy(&out.stdout).trim().to_string();
        if !ip.is_empty() {
            return Ok(ip);
        }
    }

    #[cfg(target_os = "macos")]
    {
        let out = Command::new("sh")
            .arg("-c")
            .arg("route -n get default | awk '/interface:/{print $2}' | xargs -I{} ipconfig getifaddr {}")
            .output()
            .context("failed to query default interface ip")?;
        let ip = String::from_utf8_lossy(&out.stdout).trim().to_string();
        if !ip.is_empty() {
            return Ok(ip);
        }
    }

    anyhow::bail!("unable to detect primary ipv4 address")
}

pub fn write_rollback_script(work_dir: &PathBuf, commands: &[String]) -> Result<PathBuf> {
    let path = work_dir.join("rollback.sh");
    fs::create_dir_all(work_dir)
        .with_context(|| format!("failed to create {}", work_dir.display()))?;
    let mut body = String::from("#!/usr/bin/env sh\nset -e\n");
    for c in commands {
        body.push_str(c);
        body.push('\n');
    }
    fs::write(&path, body).with_context(|| format!("failed to write {}", path.display()))?;
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        let perm = fs::Permissions::from_mode(0o700);
        fs::set_permissions(&path, perm)
            .with_context(|| format!("failed to chmod {}", path.display()))?;
    }
    Ok(path)
}

pub fn execute_rollback(path: &PathBuf) -> Result<()> {
    let status = Command::new("sh")
        .arg(path)
        .status()
        .with_context(|| format!("failed to execute rollback script: {}", path.display()))?;
    if !status.success() {
        anyhow::bail!("rollback failed with status {}", status);
    }
    Ok(())
}
